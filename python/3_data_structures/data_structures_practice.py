# Create a list named fruits
fruits = ['apple', 'banana', 'cherry', 'potato']

# Create a tuple named colors
colors = ('red', 'blue', 'green')

# Create a set named numbers
numbers = {1, 2, 3}

# Create a dictionary named person
person = {'name': 'Alex', 'age': 33}

# Add 'grape' to the fruits list
fruits.append('grape')

# Remove 'potato' from the fruits list
fruits.remove('potato')

# Check if 'apple' is in the fruits list
print('Is "apple" in fruits list?', 'apple' in fruits)

# Calculate the length of the colors tuple
print('Length of colors tuple:', len(colors))

# Add the number 4 to the numbers set
numbers.add(4)

# Create a new key-value pair in the person dictionary
