def calculate_rectangle_area(length, width):
    """ Function returns the calculated area based on length and width """
    return length * width

print(f"First area is {calculate_rectangle_area(1, 2)}")  # 2
print(f"Second area is {calculate_rectangle_area(2, 3)}")  # 6
print(f"Third area is {calculate_rectangle_area(3, 4)}")  # 12
